function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

console.log(add(5, 7));
console.log(subtract(5, 7));
console.log(multiply(5, 7));
console.log(divide(5, 7));
console.log(divide(5, 0));